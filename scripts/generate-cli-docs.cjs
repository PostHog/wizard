/**
 * Generate docs/cli.md from the CLI manifest plus the wizard-native
 * command list.
 *
 * Manifest source: prefer the build-time generated snapshot
 * (src/lib/programs/cli-manifest.generated.ts) so the docs track exactly
 * what ships. That file is gitignored and only exists after a build, so we
 * fall back to the committed bootstrap JSON on a fresh clone. Running
 * `pnpm docs:cli` after a build therefore reflects the released manifest;
 * running it on a clean checkout reflects the bootstrap. The shapes are
 * identical.
 */

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');
const MANIFEST_PATH = path.join(REPO_ROOT, 'cli-manifest.bootstrap.json');
const GENERATED_PATH = path.join(
  REPO_ROOT,
  'src',
  'lib',
  'programs',
  'cli-manifest.generated.ts',
);
const OUT_PATH = path.join(REPO_ROOT, 'docs', 'cli.md');

/**
 * Extract the manifest object literal from the generated TS module. The
 * file is `export const CLI_MANIFEST: CliManifest = <json>;`, so we slice
 * out the JSON between the assignment and its trailing semicolon. Returns
 * null if the file is absent or doesn't match the expected shape.
 */
function readGeneratedManifest() {
  if (!fs.existsSync(GENERATED_PATH)) return null;
  const ts = fs.readFileSync(GENERATED_PATH, 'utf8');
  const marker = 'export const CLI_MANIFEST: CliManifest = ';
  const start = ts.indexOf(marker);
  if (start === -1) return null;
  const jsonStart = start + marker.length;
  const jsonEnd = ts.lastIndexOf(';');
  if (jsonEnd <= jsonStart) return null;
  try {
    return JSON.parse(ts.slice(jsonStart, jsonEnd));
  } catch {
    return null;
  }
}

function loadManifest() {
  const generated = readGeneratedManifest();
  if (generated) return { manifest: generated, source: GENERATED_PATH };
  const raw = fs.readFileSync(MANIFEST_PATH, 'utf8');
  return { manifest: JSON.parse(raw), source: MANIFEST_PATH };
}

