function formatCommand(entry) {
  const path = entry.parentCommand
    ? `wizard ${entry.parentCommand} ${entry.command}`
    : `wizard ${entry.command}`;
  const defaultBadge = entry.recommended
    ? ' _(default for this family)_'
    : '';
  return `### \`${path}\`${defaultBadge}\n\n${entry.description}\n\n*Backed by context-mill skill: \`${entry.skillId}\`*`;
}

function generateMarkdown(manifest) {
  const lines = [];
  lines.push('# Wizard CLI reference');
  lines.push('');
  lines.push(
    `_Auto-generated by \`scripts/generate-cli-docs.cjs\` from manifest ` +
      `\`${manifest.buildVersion}\` (${manifest.buildTimestamp}). ` +
      `Don't hand-edit — regenerate with \`pnpm docs:cli\`._`,
  );
  lines.push('');
  lines.push(
    'The wizard\'s public command surface follows one rule: if the wizard can ' +
      'pick the variant for you (auto-detection), the command is flat. If you ' +
      'must pick, the command is a family. See ' +
      '[CONTRIBUTING.md](../CONTRIBUTING.md) for the full convention.',
  );
  lines.push('');

  const commandEntries = manifest.entries.filter((e) => e.role === 'command');

  // Group by parentCommand
  const families = new Map();
  const flat = [];
  for (const entry of commandEntries) {
    if (entry.parentCommand) {
      const bucket = families.get(entry.parentCommand) ?? [];
      bucket.push(entry);
      families.set(entry.parentCommand, bucket);
    } else {
      flat.push(entry);
    }
  }

  lines.push('## Skill-backed commands');
  lines.push('');
  lines.push(
    'These come from context-mill. Adding a new one is a context-mill PR with a ' +
      '`cli:` block on the skill\'s `config.yaml` — no wizard release required.',
  );
  lines.push('');

  if (flat.length) {
    lines.push('### Flat commands');
    lines.push('');
    for (const entry of flat.sort((a, b) => a.command.localeCompare(b.command))) {
      lines.push(formatCommand(entry));
      lines.push('');
    }
  }

  for (const [parent, entries] of [...families.entries()].sort(([a], [b]) =>
    a.localeCompare(b),
  )) {
    const sorted = entries.sort((a, b) => a.command.localeCompare(b.command));
    const defaultEntry = sorted.find((e) => e.recommended);
    const noLeafBehavior = defaultEntry
      ? `\`wizard ${parent}\` with no subcommand opens an interactive picker over the children; the default leaf (\`${defaultEntry.command}\`) is pre-highlighted, so pressing Enter runs it.`
      : `\`wizard ${parent}\` with no subcommand opens an interactive picker over the children below.`;

    lines.push(`### \`wizard ${parent}\` family`);
    lines.push('');
    lines.push(`${noLeafBehavior} \`wizard ${parent} --help\` lists the children as text.`);
    lines.push('');
    for (const entry of sorted) {
      lines.push(formatCommand(entry));
      lines.push('');
    }
  }

  lines.push('## Wizard-native commands');
  lines.push('');
  lines.push(
    'These live in the wizard repo itself, not in context-mill. They\'re ' +
      'hand-listed in `bin.ts` because they do more than dispatch a single skill ' +
      '(custom CI paths, sub-trees, post-install work, etc.).',
  );
  lines.push('');
  for (const cmd of NATIVE_COMMANDS) {
    lines.push(`### \`${cmd.name}\``);
    lines.push('');
    lines.push(cmd.description);
    lines.push('');
  }

  lines.push('## Internal flags');
  lines.push('');
  lines.push(
    'A handful of flags are accepted on every command but hidden from `--help`. ' +
      'They\'re for development, debugging, and test infrastructure. See ' +
      '[CONTRIBUTING.md](../CONTRIBUTING.md#internal-flags) for the catalog.',
  );
  lines.push('');

  return lines.join('\n');
}

function main() {
  const { manifest, source } = loadManifest();
  const markdown = generateMarkdown(manifest);
  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, markdown);
  process.stdout.write(
    `[generate-cli-docs] wrote ${OUT_PATH} from ${path.relative(REPO_ROOT, source)} (${manifest.entries.filter((e) => e.role === 'command').length} command entries, ${NATIVE_COMMANDS.length} native commands)\n`,
  );
}

main();
